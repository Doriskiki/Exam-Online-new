/**
 * rules规则转化，将element ui的表单验证数据转为uniapp支持的格式(暂不支持自定义验证)
 * @param {*} data  vue格式的表单语法
 */
export function uniappRules(data) {
	let res = {};
	if (typeof data === 'object') {
		let keys = Object.keys(data)
		keys.forEach(key => {
			let rules = data[key]
			rules.forEach(rule => {
				if (rule.errorMessage === undefined) {
					rule['errorMessage'] = rule.message
					delete rule.message
				}
				if (rule.validateFunction === undefined && rule.validator !== undefined) {
					rule['validateFunction'] = function(ruleData, value, data, callback) {
						rule.validator(ruleData, value, function(error) {
							if(typeof error === 'object') callback(error.message)
						})
					}
				}
				if (rule.pattern !== undefined) {
					let pattern = rule.pattern.toString()
					if(pattern[0] === '/') pattern = pattern.substring(1, pattern.length)
					if(pattern[pattern.length - 1] === '/') pattern = pattern.substring(0, pattern.length - 1)
					rule['pattern'] = new RegExp(pattern)
				}
			})
			res[key] = {
				'rules': rules
			}
		});
	}
	return res
}

//将后端返回的题目数据转换为前端需要的格式
export function convertQuestions(questions) {
	return questions.map(question => {
		//处理选项, 将逗号分隔的字符串转换为数组
		const options = question.options ? question.options.split(',') : []

		//处理答案, 将字符串转换为数字数组
		let answer = []
		if (question.answer) {
			if (typeof question.answer === 'string') {
				//如果是逗号分隔的字符串(多选题)
				if (question.answer.includes(',')) {
					answer = question.answer.split(',').map(item => parseInt(item.trim(), 10))
				}
				//如果是单个数字字符串(单选题或者判断题)
				else {
					answer = [parseInt(question.answer, 10)]
				}
			}
			//如果已经是数组格式
			else if (Array.isArray(question.answer)) {
				answer = question.answer.map(item => {
					return typeof item === 'string' ? parseInt(item, 10) : item
				})
			}
		}

		return {
			...question,
			options,
			answer
		}

	})
}
